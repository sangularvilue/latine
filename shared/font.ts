export function drawChar(
  pixels: Uint8Array, bufWidth: number, x: number, y: number, ch: string,
): void {
  const glyph = FONT[ch] ?? FONT[ch.toUpperCase()];
  if (!glyph) return;
  for (let row = 0; row < FONT_H; row++) {
    const bits = glyph[row]!;
    for (let col = 0; col < FONT_W; col++) {
      if (bits & (1 << (FONT_W - 1 - col))) {
        const px = x + col;
        const py = y + row;
        if (px >= 0 && px < bufWidth && py >= 0) {
          pixels[py * bufWidth + px] = 1;
        }
      }
    }
  }
}

/** Draw inverted char (white on black). */
export function drawCharInverted(
  pixels: Uint8Array, bufWidth: number, x: number, y: number, ch: string,
): void {
  const glyph = FONT[ch] ?? FONT[ch.toUpperCase()];
  if (!glyph) return;
  for (let row = 0; row < FONT_H; row++) {
    const bits = glyph[row]!;
    for (let col = 0; col < FONT_W; col++) {
      const px = x + col;
      const py = y + row;
      if (px >= 0 && px < bufWidth && py >= 0) {
        // Set pixel to opposite of glyph
        pixels[py * bufWidth + px] = (bits & (1 << (FONT_W - 1 - col))) ? 0 : 1;
      }
    }
  }
}

export function drawText(
  pixels: Uint8Array, bufWidth: number, x: number, y: number, text: string,
): void {
  let cx = x;
  for (const ch of text) {
    drawChar(pixels, bufWidth, cx, y, ch);
    cx += FONT_W + 1;
  }
}

export function measureText(text: string): number {
  if (text.length === 0) return 0;
  return [...text].length * (FONT_W + 1) - 1;
}

/** Draw text centered horizontally on a buffer of given width. */
export function drawTextCentered(
  pixels: Uint8Array, bufWidth: number, y: number, text: string,
): void {
  const w = measureText(text);
  const x = Math.floor((bufWidth - w) / 2);
  drawText(pixels, bufWidth, x, y, text);
}
